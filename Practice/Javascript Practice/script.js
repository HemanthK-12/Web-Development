let grade=prompt("Enter your marks")

if(grade<=100 && grade>=80){
    console.log("A")
}
else if(grade<=89 && grade>=70){
    console.log("B")
}
else if(grade<=69 && grade>=60){
    console.log("C")
}
else if(grade<=59 && grade>=50){
    console.log("D")
}
else if(grade<=49 && grade>=0){
    console.log("F")
}